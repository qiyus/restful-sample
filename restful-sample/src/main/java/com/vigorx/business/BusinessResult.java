package com.vigorx.business;

public class BusinessResult {
	private String entNo;
	private String entName;
	private String code;
	private String entTypeStr;
	private String registeredCapital;
	private String regorg;
	private String regorgStr;

	/**
	 * @return the entNo
	 */
	public String getEntNo() {
		return entNo;
	}

	/**
	 * @param entNo
	 *            the entNo to set
	 */
	public void setEntNo(String entNo) {
		this.entNo = entNo;
	}

	/**
	 * @return the entName
	 */
	public String getEntName() {
		return entName;
	}

	/**
	 * @param entName
	 *            the entName to set
	 */
	public void setEntName(String entName) {
		this.entName = entName;
	}

	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

	/**
	 * @param code
	 *            the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}

	/**
	 * @return the entTypeStr
	 */
	public String getEntTypeStr() {
		return entTypeStr;
	}

	/**
	 * @param entTypeStr
	 *            the entTypeStr to set
	 */
	public void setEntTypeStr(String entTypeStr) {
		this.entTypeStr = entTypeStr;
	}

	/**
	 * @return the registeredCapital
	 */
	public String getRegisteredCapital() {
		return registeredCapital;
	}

	/**
	 * @param registeredCapital
	 *            the registeredCapital to set
	 */
	public void setRegisteredCapital(String registeredCapital) {
		this.registeredCapital = registeredCapital;
	}

	/**
	 * @return the regorg
	 */
	public String getRegorg() {
		return regorg;
	}

	/**
	 * @param regorg
	 *            the regorg to set
	 */
	public void setRegorg(String regorg) {
		this.regorg = regorg;
	}

	/**
	 * @return the regorgStr
	 */
	public String getRegorgStr() {
		return regorgStr;
	}

	/**
	 * @param regorgStr
	 *            the regorgStr to set
	 */
	public void setRegorgStr(String regorgStr) {
		this.regorgStr = regorgStr;
	}

}
